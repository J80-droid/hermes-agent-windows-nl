function Invoke-HermesFullSetupWizard {
    param(
        [Parameter(Mandatory = $true)][string]$RepoRoot,
        [switch]$WhatIf
    )
    if ($WhatIf) {
        Write-Host "[WhatIf] Zou 'hermes setup --full' starten in repo." -ForegroundColor Yellow
        return 0
    }
    $hermesExe = $null
    foreach ($name in @('hermes.exe', 'hermes.cmd')) {
        $p = Join-Path $RepoRoot (Join-Path '.venv\Scripts' $name)
        if (Test-Path -LiteralPath $p) {
            $hermesExe = $p
            break
        }
    }
    if (-not $hermesExe) {
        try {
            $cmd = Get-Command hermes -ErrorAction Stop
            if ($cmd.Path) { $hermesExe = $cmd.Path }
        } catch { }
    }
    if (-not $hermesExe -or -not (Test-Path -LiteralPath $hermesExe)) {
        Write-Host "[ERROR] hermes-CLI niet gevonden (.venv\Scripts noch PATH). Installeer deps / venv, daarna: hermes setup --full" -ForegroundColor Red
        return 1
    }
    Write-Host ""
    Write-Host "[Hermes] Start installatiewizard (hermes setup --full) ..." -ForegroundColor Cyan
    Push-Location $RepoRoot
    try {
        & $hermesExe setup --full
        $code = $LASTEXITCODE
        if ($null -eq $code) { return 0 }
        return $code
    } finally {
        Pop-Location
    }
}

function New-DesktopShortcut {
    param(
        [string]$TargetBat,
        [string]$WorkDir,
        [string]$IconLocation,
        [switch]$WhatIf
    )
    $desktop = [Environment]::GetFolderPath("Desktop")
    if (-not $desktop) { return }
    $lnkPath = Join-Path $desktop "Hermes Agent.lnk"
    New-ShellShortcut -LnkPath $lnkPath -TargetPath "cmd.exe" -Arguments ('/k "' + $TargetBat + '"') `
        -WorkDir $WorkDir -IconLocation $IconLocation `
        -Description "Hermes Agent - Windows launcher (logo)" -WhatIf:$WhatIf
}

$root = Resolve-RepoRoot -Explicit $RepoRoot
$win = Join-Path $root "windows"

if (-not $WhatIf) {
    New-Item -ItemType Directory -Path $win -Force | Out-Null
}

$iconLoc = Get-HermesWhiteShortcutIcon -WindowsDir $win -WhatIf:$WhatIf

Write-WindowsSetupCmdBat -WindowsDir $win -WhatIf:$WhatIf
if (-not $WhatIf) { Write-Host ('[OK] ' + (Join-Path $win 'setup_hermes_windows.bat')) -ForegroundColor Green }

$openSetupSrc = Join-Path $PSScriptRoot "OPEN_SETUP.bat"
$openSetupDst = Join-Path $win "OPEN_SETUP.bat"
if (-not $WhatIf) {
    if (Test-Path -LiteralPath $openSetupSrc) {
        Copy-Item -LiteralPath $openSetupSrc -Destination $openSetupDst -Force
        Write-Host ('[OK] ' + $openSetupDst) -ForegroundColor Green
    } else {
        Write-Warning "OPEN_SETUP.bat ontbreekt in scripts\windows — wizard-snelkoppeling niet gekopieerd."
    }
}

Write-HermesUpdateCmdBat -WindowsDir $win -WhatIf:$WhatIf
if (-not $WhatIf) { Write-Host ('[OK] ' + (Join-Path $win 'hermes_update.bat')) -ForegroundColor Green }

$logoBat = Join-Path $win "Hermes_met_logo.bat"
$launchBat = Join-Path $win "launch_hermes.bat"

if ($ForceLogoBat -or -not (Test-Path -LiteralPath $logoBat)) {
    Write-LogoBat -WindowsDir $win -WhatIf:$WhatIf
    if (-not $WhatIf) { Write-Host ('[OK] Logo-launcher: ' + $logoBat) -ForegroundColor Green }
} else {
    Write-Host '[--] Logo-launcher bestaat al (gebruik -ForceLogoBat om te overschrijven).' -ForegroundColor Yellow
}

if ($ForceLaunchBat) {
    Write-MinimalLaunchBat -WindowsDir $win -RepoRootPath $root -WhatIf:$WhatIf
    if (-not $WhatIf) { Write-Host '[OK] launch_hermes.bat geschreven (-ForceLaunchBat).' -ForegroundColor Green }
} elseif (-not (Test-Path -LiteralPath $launchBat)) {
    Write-MinimalLaunchBat -WindowsDir $win -RepoRootPath $root -WhatIf:$WhatIf
    if (-not $WhatIf) { Write-Host '[OK] Minimaal launch_hermes.bat aangemaakt (was afwezig).' -ForegroundColor Green }
} else {
    Write-Host '[--] launch_hermes.bat bestaat al - niet overschreven.' -ForegroundColor Yellow
}

if (-not $NoShortcut) {
    if (Test-Path -LiteralPath $logoBat) {
        New-DesktopShortcut -TargetBat $logoBat -WorkDir $root -IconLocation $iconLoc -WhatIf:$WhatIf
        if (-not $WhatIf) {
            $desk = [Environment]::GetFolderPath("Desktop")
            Write-Host ('[OK] Snelkoppeling: ' + (Join-Path $desk 'Hermes Agent.lnk')) -ForegroundColor Green
        }
    } else {
        Write-Warning "Snelkoppeling overgeslagen: logo-bat ontbreekt."
    }
    $openBatWin = Join-Path $win "OPEN_SETUP.bat"
    if (Test-Path -LiteralPath $openBatWin) {
        $deskOs = [Environment]::GetFolderPath("Desktop")
        if ($deskOs) {
            $openLnkDesk = Join-Path $deskOs "Hermes Open Setup.lnk"
            New-ShellShortcut -LnkPath $openLnkDesk -TargetPath "cmd.exe" -Arguments ('/k "' + $openBatWin + '"') `
                -WorkDir $root -IconLocation $iconLoc `
                -Description "Hermes — volledige setup-wizard (OPEN_SETUP)" -WhatIf:$WhatIf
            if (-not $WhatIf) {
                Write-Host ('[OK] Snelkoppeling: ' + $openLnkDesk) -ForegroundColor Green
            }
        }
    }
}

if (-not $NoTaskbarLinks) {
    $setupBat = Join-Path $win "setup_hermes_windows.bat"
    $updateBat = Join-Path $win "hermes_update.bat"
    $openBatWin = Join-Path $win "OPEN_SETUP.bat"
    $setupLnk = Join-Path $win "Hermes - setup Windows - naar taakbalk slepen.lnk"
    $updateLnk = Join-Path $win "Hermes - update - naar taakbalk slepen.lnk"
    $openLnkTb = Join-Path $win "Hermes - Open Setup - naar taakbalk slepen.lnk"
    if ((Test-Path -LiteralPath $setupBat) -and (Test-Path -LiteralPath $updateBat)) {
        New-ShellShortcut -LnkPath $setupLnk -TargetPath $setupBat -Arguments "" -WorkDir $root `
            -IconLocation $iconLoc -Description "Hermes: Windows-setup (logo, snelkoppelingen)" -WhatIf:$WhatIf
        New-ShellShortcut -LnkPath $updateLnk -TargetPath $updateBat -Arguments "" -WorkDir $root `
            -IconLocation $iconLoc -Description "Hermes: update via launch_hermes.bat" -WhatIf:$WhatIf
        if (-not $WhatIf) {
            Write-Host ('[OK] Taakbalk-stijl: ' + $setupLnk) -ForegroundColor Green
            Write-Host ('[OK] Taakbalk-stijl: ' + $updateLnk) -ForegroundColor Green
        }
        if (Test-Path -LiteralPath $openBatWin) {
            New-ShellShortcut -LnkPath $openLnkTb -TargetPath $openBatWin -Arguments "" -WorkDir $root `
                -IconLocation $iconLoc -Description "Hermes: volledige setup-wizard (OPEN_SETUP.bat)" -WhatIf:$WhatIf
            if (-not $WhatIf) {
                Write-Host ('[OK] Taakbalk-stijl: ' + $openLnkTb) -ForegroundColor Green
            }
        }
    }
}

$runHermesWizard = $FullSetup -or ($env:HERMES_SETUP_FULL_SETUP -eq '1')
$batLaunchedFromCmd = ($env:HERMES_WINDOWS_BAT_PARENT -eq '1')
if ($runHermesWizard -and -not $batLaunchedFromCmd) {
    $wizExit = Invoke-HermesFullSetupWizard -RepoRoot $root -WhatIf:$WhatIf
    if ($wizExit -ne 0) { exit $wizExit }
}

$ragExtras = Join-Path $root "windows\scripts\install_rag_extras.ps1"
if ((Test-Path -LiteralPath $ragExtras) -and -not $WhatIf) {
    Write-Host ""
    Write-Host "[INFO] RAG: pip extra [rag] + MCP lancedb-knowledge..." -ForegroundColor Cyan
    try {
        & powershell -NoProfile -ExecutionPolicy Bypass -File $ragExtras -RepoRoot $root -Quiet
        Write-Host "[OK] RAG-extras (pip + MCP waar mogelijk)." -ForegroundColor Green
    } catch {
        Write-Warning "RAG-extras mislukt — handmatig: windows\scripts\install_rag_extras.ps1"
    }
}

Write-Host ""
Write-Host 'Klaar: windows\setup_hermes_windows.bat + Hermes Open Setup.lnk + Hermes - * - naar taakbalk slepen.lnk (wit icoon).' -ForegroundColor Cyan
Write-Host 'Tip: draai daarna windows\REFRESH_TASKBAR_SHORTCUTS.bat zodat ook overige Hermes-icoontjes wit update-icoon pakken als hermes_taskbar_white.ico bestaat.' -ForegroundColor DarkGray