function Get-HermesWhiteShortcutIcon {
    param(
        [string]$WindowsDir,
        [switch]$WhatIf
    )
    $ico = Join-Path $WindowsDir "hermes_taskbar_white.ico"
    if ($WhatIf) {
        return ($ico + ",0")
    }
    try {
        Add-Type -AssemblyName System.Drawing -ErrorAction Stop
        $size = 48
        $bmp = New-Object System.Drawing.Bitmap ($size), ($size), ([System.Drawing.Imaging.PixelFormat]::Format32bppArgb)
        $g = [System.Drawing.Graphics]::FromImage($bmp)
        $g.SmoothingMode = [System.Drawing.Drawing2D.SmoothingMode]::AntiAlias
        $g.Clear([System.Drawing.Color]::FromArgb(0, 0, 0, 0))
        $pen = New-Object System.Drawing.Pen ([System.Drawing.Color]::White, 5)
        $pen.StartCap = [System.Drawing.Drawing2D.LineCap]::Round
        $pen.EndCap = [System.Drawing.Drawing2D.LineCap]::Round
        $g.DrawLine($pen, 10, 8, 10, 40)
        $g.DrawLine($pen, 32, 8, 32, 40)
        $g.DrawLine($pen, 10, 24, 32, 24)
        $pen.Dispose()
        $g.Dispose()
        $icon = [System.Drawing.Icon]::FromHandle($bmp.GetHicon())
        try {
            $fs = [System.IO.File]::Create($ico)
            $icon.Save($fs)
            $fs.Close()
        } finally {
            $icon.Dispose()
            $bmp.Dispose()
        }
        return ($ico + ",0")
    } catch {
        Write-Warning ("Witte .ico niet gegenereerd (" + $_.Exception.Message + "); fallback imageres.")
        return ($env:SystemRoot + "\System32\imageres.dll,1")
    }
}

function New-ShellShortcut {
    param(
        [string]$LnkPath,
        [string]$TargetPath,
        [string]$Arguments,
        [string]$WorkDir,
        [string]$IconLocation,
        [string]$Description,
        [switch]$WhatIf
    )
    if ($WhatIf) {
        Write-Host ('[WhatIf] Snelkoppeling: ' + $LnkPath)
        return
    }
    $w = New-Object -ComObject WScript.Shell
    $s = $w.CreateShortcut($LnkPath)
    $s.TargetPath = $TargetPath
    $s.Arguments = $Arguments
    $s.WorkingDirectory = $WorkDir
    $s.WindowStyle = 1
    if ($IconLocation) { $s.IconLocation = $IconLocation }
    if ($Description) { $s.Description = $Description }
    $s.Save()
}

